//Reference types - Objects

// Array
const array1 = [1,2,3,4,5,6];
console.log(array1 + " is a type of " + typeof array1); 
    //Returns Object as type.

// Object Literal
const objliteral = {

    name: 'Yash',
    surname: 'Panchal',
    state: 'Gujarat'

}
console.log(objliteral + " is a type of " + typeof objliteral); 


// Date
const today = new Date();
console.log(today + " is an " + typeof today );

//With reference types they are being accessed fromt he memory as the reference and not the exact value.


